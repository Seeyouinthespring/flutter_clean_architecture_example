class PersonView{
  final String gender;
  final String fullName;
  final String phone;
  final String email;
  final String photo;
  final String address;
  final int age;
  final String birthDate;

  PersonView({
    required this.gender,
    required this.fullName,
    required this.phone,
    required this.email,
    required this.photo,
    required this.address,
    required this.age,
    required this.birthDate
  });
}